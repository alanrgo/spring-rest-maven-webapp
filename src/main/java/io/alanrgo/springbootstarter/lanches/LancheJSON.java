package io.alanrgo.springbootstarter.lanches;

public class LancheJSON {
	private int alface;
	private int bacon;
	private int hamb;
	private int ovo;
	private int queijo;
	
	public int getAlface() {
		return alface;
	}
	public void setAlface(int alface) {
		this.alface = alface;
	}
	public int getBacon() {
		return bacon;
	}
	public void setBacon(int bacon) {
		this.bacon = bacon;
	}
	public int getHamb() {
		return hamb;
	}
	public void setHamb(int hamb) {
		this.hamb = hamb;
	}
	public int getOvo() {
		return ovo;
	}
	public void setOvo(int ovo) {
		this.ovo = ovo;
	}
	public int getQueijo() {
		return queijo;
	}
	public void setQueijo(int queijo) {
		this.queijo = queijo;
	}
	
}
